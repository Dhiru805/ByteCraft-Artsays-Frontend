import { useState, useEffect } from "react";
import { useNavigate } from "react-router-dom";
import { ImArrowUpRight2 } from "react-icons/im";
import { Star } from "lucide-react";
import getAPI from "../../../api/getAPI";
import ArtlconSkeleton from "../../../Component/Skeleton/ArtIconSkeleton";
const ArtIcon = () => {
  const [data, setData] = useState(null);
  const [celebrities, setCelebrities] = useState([]);
  const [loading, setLoading] = useState(true);
  const navigate = useNavigate();

  useEffect(() => {
    const fetchData = async () => {
      try {
        const pageRes = await getAPI("/api/homepage/published");
        const homepage = pageRes.data.data;
        if (!homepage?._id) throw new Error("No published homepage found");

        const sectionRes = await getAPI(
          `/api/homepage-sections/art-icon/${homepage._id}`
        );
        if (!sectionRes.data.success || !sectionRes.data.data)
          throw new Error("Art Icon section not found");

        setData(sectionRes.data.data);

        const celebRes = await getAPI("/api/celebrities");
        if (!celebRes?.hasError) {
          setCelebrities(celebRes.data.data || []);
        } else {
          console.log("Error fetching celebrities:", celebRes);
        }
      } catch (err) {
        console.error(err);
      } finally {
        setLoading(false);
      }
    };

    fetchData();
  }, []);

  const slugify = (text) => {
    return text
      .toLowerCase()
      .trim()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "");
  };

  if (loading) return <div><ArtlconSkeleton /></div>;
  if (!data) return <div>No Art Icon section available</div>;

    return (
    <div className="bg-[#F8F8F8] font-[poppins] py-12">
      <div className="max-w-[1440px] mx-auto px-4 md:!px-0">
       
        <div className="flex flex-col md:flex-row md:items-end justify-between gap-6 align-items-center mb-8">
          <div className="flex flex-col gap-6">
            <h1 className="text-3xl md:text-5xl font-black text-gray-900 tracking-tighter">
              {data.heading}
            </h1>
            <p className="text-gray-500 text-lg max-w-2xl font-medium leading-relaxed">
              {data.description}
            </p>
          </div>
          {data.buttonName && (
            <button
              onClick={() => navigate(data.buttonLink || "/celebrity")}
              className="hidden lg:block bg-[#6F4D34] text-white px-8 py-4 rounded-2xl font-black uppercase tracking-widest text-sm hover:bg-gray-900 transition-all shadow-lg shadow-[#6F4D34]/20 transform active:scale-95"
            >
              {data.buttonName}
            </button>
          )}
        </div>

        <div className="flex sm:grid sm:grid-cols-2 lg:grid-cols-4 gap-3 mt-6 overflow-x-auto snap-x snap-mandatory no-scrollbar" style={{ scrollbarWidth: "none" }}>
          {celebrities.length > 0 ? (
            celebrities.slice(0, 8).map((celebrity, index) => (
              <div
                key={celebrity._id}
                className="min-w-[77%] sm:min-w-0 snap-start group bg-white rounded-[2rem] overflow-hidden shadow-sm hover:shadow-xl transition-all duration-500 border border-gray-100 flex flex-col h-full"
              >
                <div className="p-4 md:p-6 text-center">
                  <h2 className="text-base md:text-xl font-bold text-gray-900 group-hover:text-[#6F4D34] transition-colors line-clamp-1">
                    {celebrity.artistName || ""}
                  </h2>
                </div>

                <div className="relative aspect-square overflow-hidden bg-gray-50 border-y border-gray-100">
                  <img
                    src={celebrity.profilePicture || celebrity.image || "/herosectionimg/1.jpg"}
                    alt={celebrity.artistName || ""}
                    className="w-full h-full object-cover transition-transform duration-700 group-hover:scale-105"
                  />
                </div>

                <div className="p-4 md:p-6 mt-auto">
                  <div className="flex items-center justify-between mb-4">
                    <div className="flex items-center gap-1 md:gap-2">
                      <Star size={14} className="text-yellow-400 fill-yellow-400 md:w-4 md:h-4" />
                      <span className="text-[10px] md:text-sm font-bold text-gray-900">
                        {celebrity.yearsActiveInArt || 0}+ Years Active
                      </span>
                    </div>
                    <div className="text-[8px] md:text-[10px] font-bold text-[#6F4D34] bg-[#6F4D34]/5 px-2 md:px-3 py-1 rounded-full uppercase tracking-wider">
                      Featured
                    </div>
                  </div>
                  
                  <button
                    onClick={() =>
                      navigate(`/celebrity/${slugify(celebrity.artistName)}`, {
                        state: { celebrity },
                      })
                    }
                    className="w-full flex items-center justify-center gap-2 bg-[#6F4D34] text-white hover:!text-[#6F4D34] py-2 md:py-3 border border-gray-100 rounded-xl font-bold hover:bg-[#ffffff] transition-colors shadow-sm text-xs md:text-base"
                  >
                    View Collection
                    <ImArrowUpRight2 size={12} className="md:w-[14px] md:h-[14px]" />
                  </button>
                </div>
              </div>
            ))
          ) : (
            <div>No celebrities available</div>
          )}
        </div>
      </div>
    </div>
  );
};

export default ArtIcon;
